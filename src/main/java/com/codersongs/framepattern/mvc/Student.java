package com.codersongs.framepattern.mvc;

public class Student {
    private String realNo;
    private String name;

    public String getRealNo() {
        return realNo;
    }

    public void setRealNo(String realNo) {
        this.realNo = realNo;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
